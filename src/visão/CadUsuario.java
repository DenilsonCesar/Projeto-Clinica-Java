    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo USUÁRIO para continuar!");
            //Para ir direto para o campo nome para o mais rapido ser preenchido
            txtUsuario.requestFocus();
        } else if (txtSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo SENHA para continuar!");
            txtSenha.requestFocus();
        } else if (txtCSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo CONFIRMAR SENHA para continuar!");
            txtCSenha.requestFocus();
        } else if (txtSenha.getText().equals(txtCSenha.getText())) {
            if (flag == 1) {
                //usuario.setUsuCod(Integer.parseInt(txtCod.getText()));
                usuario.setUsuNome(txtUsuario.getText());
                usuario.setUsuSenha(txtSenha.getText());
                usuario.setUsuTipo(String.valueOf(cboTipo.getSelectedItem()));
                dao.Salvar(usuario);
                preencherTabela("select * from usuarios order by usu_nome");
                txtCod.setText("");
                txtUsuario.setText("");
                txtSenha.setText("");
                txtCSenha.setText("");
                txtUsuario.setEnabled(false);
                txtSenha.setEnabled(false);
                cboTipo.setEnabled(false);
                btnSalvar.setEnabled(false);
                txtCSenha.setEnabled(false);
                btnNovo.setEnabled(true);
                btnCancelar.setEnabled(false);
            } else {
                usuario.setUsuCod(Integer.parseInt(txtCod.getText()));
                usuario.setUsuNome(txtUsuario.getText());
                usuario.setUsuSenha(txtSenha.getText());
                usuario.setUsuTipo(String.valueOf(cboTipo.getSelectedItem()));
                dao.Alterar(usuario);
                preencherTabela("select * from usuarios order by usu_nome");
                txtCod.setText("");
                txtUsuario.setText("");
                txtSenha.setText("");
                txtCSenha.setText("");
                txtUsuario.setEnabled(false);
                txtSenha.setEnabled(false);
                cboTipo.setEnabled(false);
                btnSalvar.setEnabled(false);
                txtCSenha.setEnabled(false);
                btnNovo.setEnabled(true);
                btnCancelar.setEnabled(false);
                btnAlterar.setEnabled(false);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Senhas não compativeis, Tente novamente!");
            txtCSenha.requestFocus();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

