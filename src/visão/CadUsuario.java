    private void tblUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuarioMouseClicked
        String nome_usuario = "" + tblUsuario.getValueAt(tblUsuario.getSelectedRow(), 1);
        conexao0.conexao();
        conexao0.executeSQL("select * from usuarios where usu_nome = '" + nome_usuario + "'");
        try {
            conexao0.rs.first();
            txtCod.setText(String.valueOf(conexao0.rs.getInt("usu_cod")));
            txtUsuario.setText(conexao0.rs.getString("usu_nome"));
            txtSenha.setText(String.valueOf(conexao0.rs.getInt("usu_senha")));
            txtCSenha.setText(String.valueOf(conexao0.rs.getInt("usu_senha")));
            cboTipo.setSelectedItem(conexao0.rs.getString("usu_tipo"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar dados!" + ex);
        }
        btnNovo.setEnabled(false);
        btnSalvar.setEnabled(false);
        btnAlterar.setEnabled(true);
        btnExcluir.setEnabled(true);
        btnCancelar.setEnabled(true);
        txtCod.setEnabled(false);
        txtUsuario.setEnabled(false);
        txtCSenha.setEnabled(false);
        txtSenha.setEnabled(false);
        cboTipo.setEnabled(false);
    }//GEN-LAST:event_tblUsuarioMouseClicked

