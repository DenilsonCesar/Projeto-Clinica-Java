    private void tabelaPesquisasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaPesquisasMouseClicked
        String nome_medico = "" + tabelaPesquisas.getValueAt(tabelaPesquisas.getSelectedRow(), 1);
        conexao0.conexao();
        conexao0.executeSQL("select * from medicos where nome_medico = '" + nome_medico + "'");
        try {
            conexao0.rs.first();
            txtId.setText(String.valueOf(conexao0.rs.getInt("cod_medico")));
            txtNomeMedico.setText(conexao0.rs.getString("nome_medico"));
            txtCrm.setText(String.valueOf(conexao0.rs.getInt("crm_medico")));
            cboEsper.setSelectedItem(conexao0.rs.getString("espercializacao_medico"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar dados!" + ex);
        }
        btnEditar.setEnabled(true);
        btnExcluir.setEnabled(true);
    }//GEN-LAST:event_tabelaPesquisasMouseClicked

    public void preencherTabela(String sql) {
        //Esta instanciando as listas para fazer as linhas da tabela
        ArrayList dados = new ArrayList();
        //Instanciando a String sendo que e um vetor
        //{} dentro das chaves vai ser o nome das colunas
        //Esta instanciando as colunas para fazer as colunas da tabela
        String[] colunas = new String[]{"ID", "Nome", "Espercialidade", "CRM"};
        //conexao0 e a instanciação da classe conexaoBD, e conexao e o metodo que está dentro dela que faz a conexão com o bd
        conexao0.conexao();
        //conexao0, é a conexao com o banco
        //Vai fazer a execução do codigo sql
        conexao0.executeSQL(sql);
        try {
            //vai pegar o primeiro resultado da conexao0
            conexao0.rs.first();
            do {//trazendo os campos do banco para a tabela
                dados.add(new Object[]{conexao0.rs.getInt("cod_medico"), conexao0.rs.getString("nome_medico"), conexao0.rs.getString("espercializacao_medico"), conexao0.rs.getInt("crm_medico")});
            }//Equanto estiver dados ele vai ficar percorrendo este registro, ele vai andar nas posicões pois não existe uma limitação e sim uma condição para ele percorrer tudo que ele achar
            while (conexao0.rs.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Médico não cadastrado!\nPesquise por outro médico!");
        }
        //Dados a quantidade de linhas/ e colunas a quantidade de colunas
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        tabelaPesquisas.setModel(modelo);
        //setPreferreWidth ela colocando o tamanho da tabela
        //getColumn(0) pq do (0)? pq a tabela começa do 0 como estamos trabalhado com um array ele começa da posição 0
        //getColumnModel é a coluna, getColumn e de onde vai começa a lista sendo que 0 e o ID e setPreferredWidth(23) é a lagura do campo id que vai receber os ids
        tabelaPesquisas.getColumnModel().getColumn(0).setPreferredWidth(30);
        //setResizable e para o usuario não poder mexer no tamanho dos camposda tabela com o mouse esticado-a
        tabelaPesquisas.getColumnModel().getColumn(0).setResizable(false);
        tabelaPesquisas.getColumnModel().getColumn(1).setPreferredWidth(180);
        tabelaPesquisas.getColumnModel().getColumn(1).setResizable(false);
        tabelaPesquisas.getColumnModel().getColumn(2).setPreferredWidth(80);
        tabelaPesquisas.getColumnModel().getColumn(2).setResizable(false);
        tabelaPesquisas.getColumnModel().getColumn(3).setPreferredWidth(80);
        tabelaPesquisas.getColumnModel().getColumn(3).setResizable(false);
        //Não vai poder reodenar o cabeçario
        tabelaPesquisas.getTableHeader().setReorderingAllowed(false);
        //O usuario não poderá redimecionar o tamanho da tabela
        tabelaPesquisas.setAutoResizeMode(tabelaPesquisas.AUTO_RESIZE_OFF);
        //Para o usuario poder somente selecionar um unico usuario da tabela por vez
        tabelaPesquisas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }

