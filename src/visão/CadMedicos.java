    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNomeMedico.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo NOME para continuar!");
            //Para ir direto para o campo nome para o mais rapido ser preenchido
            txtNomeMedico.requestFocus();
        } else if (txtCrm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha o campo CRM para continuar!");
            txtCrm.requestFocus();
        } else 
            if (flag == 1) {
            medico.setNome(txtNomeMedico.getText());
            medico.setCrm(Integer.parseInt(txtCrm.getText()));
            medico.setEspercializacao(String.valueOf(cboEsper.getSelectedItem()));
            dao.Salvar(medico);
            txtNomeMedico.setText("");
            txtCrm.setText("");
            txtNomeMedico.setEnabled(false);
            txtCrm.setEnabled(false);
            cboEsper.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnCancelar.setEnabled(false);
            preencherTabela("select * from medicos order by nome_medico");
        } else {
            medico.setCodigo(Integer.parseInt(txtId.getText()));
            medico.setNome(txtNomeMedico.getText());
            medico.setCrm(Integer.parseInt(txtCrm.getText()));
            medico.setEspercializacao(String.valueOf(cboEsper.getSelectedItem()));
            dao.Editar(medico);
            txtId.setText("");
            txtNomeMedico.setText("");
            txtCrm.setText("");
            txtNomeMedico.setEnabled(false);
            txtCrm.setEnabled(false);
            cboEsper.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnNovo.setEnabled(true);
            btnCancelar.setEnabled(false);
            preencherTabela("select * from medicos order by nome_medico");

        }
    }//GEN-LAST:event_btnSalvarActionPerformed

